package com.yubao.model;

public class Tag {
    private String id;

    private String tagto;

    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTagto() {
        return tagto;
    }

    public void setTagto(String tagto) {
        this.tagto = tagto;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}